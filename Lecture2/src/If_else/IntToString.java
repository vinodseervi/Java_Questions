package If_else;

import java.util.Scanner;

public class IntToString {

	public static void main(String[] args) {
		Scanner s = new Scanner(System.in);
		int a = s.nextInt();
		
		if(a == 1) {
			System.out.println("One");
		}
		else if(a == 2) {
			System.out.println("Two");
		}
		else if(a ==3) {
			System.out.println("Three");
		}

	}

}
